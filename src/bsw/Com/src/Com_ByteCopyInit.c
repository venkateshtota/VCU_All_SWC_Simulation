/*
 **********************************************************************************************************************
 Function name    : Com_ByteCopyInit
 Description      : Service for copying the value passed to destination for UINT8_N Signal
 Parameter        : Pointer to Destination, Initial value and Length
 Return value     : None
 **********************************************************************************************************************
*/
#define COM_START_SEC_CODE
#include "Com_MemMap.h"
void Com_ByteCopyInit(uint8 * dest_pu8, uint32 initVal_u32, uint32 length_u32)
{
    while(length_u32 > COM_ZERO)
    {
        *(dest_pu8) = (uint8)(initVal_u32 >> (8UL*(length_u32-1UL)));
        dest_pu8++;
        --length_u32;
    }

}
#define COM_STOP_SEC_CODE
#include "Com_MemMap.h"

